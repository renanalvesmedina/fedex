package com.mercurio.lms.layoutNfse.model.rps;

public class Endereco {
	private String Endereco;
	private String Numero;
	private String Complemento;
	private String Bairro;
	private String CodigoMunicipio;
	private String CodigoMunicipioSiafi;
	private String Uf;
	private String Cep;
	private Integer CodigoPais;
	private String CodigoMunicipioOutros;
	
	public String getEndereco() {
		return Endereco;
	}
	public void setEndereco(String endereco) {
		Endereco = endereco;
	}
	public String getNumero() {
		return Numero;
	}
	public void setNumero(String numero) {
		Numero = numero;
	}
	public String getComplemento() {
		return Complemento;
	}
	public void setComplemento(String complemento) {
		Complemento = complemento;
	}
	public String getBairro() {
		return Bairro;
	}
	public void setBairro(String bairro) {
		Bairro = bairro;
	}
	public String getUf() {
		return Uf;
	}
	public void setUf(String uf) {
		Uf = uf;
	}
	public String getCep() {
		return Cep;
	}
	public void setCep(String cep) {
		Cep = cep;
	}
	public Integer getCodigoPais() {
		return CodigoPais;
	}
	public void setCodigoPais(Integer codigoPais) {
		CodigoPais = codigoPais;
	}
	public String getCodigoMunicipio() {
		return CodigoMunicipio;
	}
	public void setCodigoMunicipio(String codigoMunicipio) {
		CodigoMunicipio = codigoMunicipio;
	}
	public String getCodigoMunicipioSiafi() {
		return CodigoMunicipioSiafi;
	}
	public void setCodigoMunicipioSiafi(String codigoMunicipioSiafi) {
		CodigoMunicipioSiafi = codigoMunicipioSiafi;
	}
	public String getCodigoMunicipioOutros() {
		return CodigoMunicipioOutros;
	}
	public void setCodigoMunicipioOutros(String codigoMunicipioOutros) {
		CodigoMunicipioOutros = codigoMunicipioOutros;
	}
	
}
